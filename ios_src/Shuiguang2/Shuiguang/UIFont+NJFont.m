{
    return [UIFont fontWithName:@"FZPWJW" size:fontSize];
}

+ (UIFont*)mediumFontWithSize:(CGFloat)fontSize
{
    return  [UIFont fontWithName:@"FZPWJW" size:fontSize];
}

+ (UIFont*)regularFontWithSize:(CGFloat)fontSize
{
    return  [UIFont fontWithName:@"FZPWJW" size:fontSize];
}

+ (UIFont*)lightFontWithSize:(CGFloat)fontSize;
{
    return  [UIFont fontWithName:@"FZPWJW" size:fontSize];
}



@end
